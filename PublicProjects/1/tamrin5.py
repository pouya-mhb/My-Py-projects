number = int(input("please enter a number : "))

number = 5
k = 2 * number - 2
for i in range(0, number):
    for j in range(0, k):
        print(end=" ")
    k = k - 1
    for j in range(0, i + 1):
        print("* ", end="")
    print("")
    
